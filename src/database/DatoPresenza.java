package database;

public class DatoPresenza {

	private String nome;
	private String cognome;
	private int codCorso;
	private int cordLezione;
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getCognome() {
		return cognome;
	}
	public void setCognome(String cognome) {
		this.cognome = cognome;
	}
	public int getCodCorso() {
		return codCorso;
	}
	public void setCodCorso(int codCorso) {
		this.codCorso = codCorso;
	}
	public int getCordLezione() {
		return cordLezione;
	}
	public void setCordLezione(int cordLezione) {
		this.cordLezione = cordLezione;
	}

}
